import turtle

wn = turtle.Screen()

turtle.forward(50)  # двигаемся на 50 пикселей
turtle.setheading(90)  # устанавливаем направление на 90 градусов
turtle.forward(100)  # двигаемся на 100 пикселей
turtle.setheading(180)  # устанавливаем направление на 180 градусов
turtle.forward(50)  # двигаемся на 50 пикселей
turtle.setheading(270)  # устанавливаем направление на 270 градусов
turtle.forward(100)  # двигаемся на 100 пикселей

wn.mainloop()